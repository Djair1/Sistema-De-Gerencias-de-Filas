    public void CarregarTabela() {

        try {

            DefaultTableModel modelocp = (DefaultTableModel) tabelacp.getModel();
            DefaultTableModel modelocpr = (DefaultTableModel) tabelacpr.getModel();
            modelocp.setNumRows(0);
            modelocpr.setNumRows(0);
            for (Clientes cliente : ClienteDao.getInstance().procurartodos()) {

                modelocp.addRow(new Object[]{cliente.getId(), cliente.getCliente(), cliente.getGuicheMesa(), cliente.getSenha()});

            }
            for (ClientePrioritario cpro : ClienteDaoPrioritario.getInstance().procurartodos()) {

                modelocpr.addRow(new Object[]{cpro.getId(), cpro.getCliente(), cpro.getGuicheMesa(), cpro.getSenha()});
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "ERRO NO CARREGAMENTO DAS TABELAS !");
        }
    }

