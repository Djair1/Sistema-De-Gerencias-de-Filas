    public void gerarSenhaPadrao(Integer i,String n, String s){
    
       String nome,sobre; 
       Integer m;
        
       
        
         if(n!="" && s!=""){ m=i;nome=n;sobre=s;}
         else{
        m = Integer.parseInt(this.mg.getText());
        nome = this.nome.getText();
        sobre = this.sobrenome.getText();}
        
       

        try {
            if (m >= 1) {
             
                GerarSenhas gs = new GerarSenhas();
                gs.gerarSenhaPadrao(m, nome, sobre);
                Limpar();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "ERRO Definir Guinchê/ Mesa  !");
        }
        CarregarTabela();
    
    
    }
    
