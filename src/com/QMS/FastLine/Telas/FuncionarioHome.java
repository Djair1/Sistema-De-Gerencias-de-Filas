    public void chamarPrioritario() {
        try {
            this.identificadorP++;
            ClientePrioritario cpr = ClienteDaoPrioritario.getInstance().getById(this.identificadorP);
            this.senha.setText(cpr.getSenha());
            this.guinche.setText(String.valueOf(cpr.getGuicheMesa()));
            tp.AlertarPrioritario();
            String s = senha.getText();
            String g = guinche.getText();
            tp.alimentarPainel(s, g);
            tp.CarregarUltimos(s, g);
            ac.SomReproduzir("Alerta");

        } catch (Exception e) {
            this.fnlpr = +2;
            JOptionPane.showMessageDialog(this, "Fim da Lista de Clientes Prioritarios");
        }

    }

